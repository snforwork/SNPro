package co.jp.chapter07;

public class Human {

/*
 * 全局变量 实体属性描述
 */
	//名前
	private String name ;
	//年齢
	private int age ;
	//性別 男：1 女：0
	private int sex;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}

		

}
